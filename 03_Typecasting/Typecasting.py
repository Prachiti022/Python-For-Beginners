
#Implicit Typecasting
i = 2
f = 2.2
print("i = ", i, "  Type = ", type(i))
print("f = ", f, "  Type = ", type(f))
ans = i + f
print("Addition: ", ans, "  Type = ", type(ans))

print(" ")
print(" ")
