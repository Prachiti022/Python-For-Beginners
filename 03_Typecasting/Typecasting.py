#Explicit Typecasting
i = 5
s = "10"
print("i = ", i, "  Type = ", type(i))
print("s = ", s, "  Type = ", type(s))
ans = i + int(s)
print("Addition: ", ans, "  Type = ", type(ans))

print(" ")
print(" ")
