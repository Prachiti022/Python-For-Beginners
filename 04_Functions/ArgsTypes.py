# Function without argument
def greet():
    print("Hello, Welcome to Python!")

# Function with argument
def greet_name(name):
    print("Hello,", name)

# Function returning a value
def add_numbers(a, b):
    return a + b

# Calling functions
greet()                       # Function without argument
greet_name("Prachiti")        # Function with argument
result = add_numbers(10, 20)  # Function returning value
print("Sum:", result)
